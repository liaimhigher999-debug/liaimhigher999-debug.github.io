import type { AssetCredit, Era, ExplorationClue, PlatformLinks, Track } from './types'

const listenLinks = (query: string): PlatformLinks => {
  const encoded = encodeURIComponent(`The 1975 ${query}`)

  return {
    spotify: `https://open.spotify.com/search/${encoded}`,
    appleMusic: `https://music.apple.com/us/search?term=${encoded}`,
    youtube: `https://www.youtube.com/results?search_query=${encoded}`,
  }
}

const track = (
  title: string,
  slug: string,
  featured: boolean,
  excerpt: string,
  note: string,
): Track => ({
  title,
  slug,
  featured,
  excerpt,
  note,
  links: listenLinks(title),
})

